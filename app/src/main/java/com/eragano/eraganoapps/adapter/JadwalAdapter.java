public class JadwalAdapter {


    /**
     * judul_langkah : Penyemaian
     * langkah : Lakukan Penyemaian Bibit Cabe
     * jam_mulai : 8
     * jam_akhir : 10
     */

    private String judul_langkah;
    private String langkah;
    private String jam_mulai;
    private String jam_akhir;

    public String getJudul_langkah() {
        return judul_langkah;
    }

    public void setJudul_langkah(String judul_langkah) {
        this.judul_langkah = judul_langkah;
    }

    public String getLangkah() {
        return langkah;
    }

    public void setLangkah(String langkah) {
        this.langkah = langkah;
    }

    public String getJam_mulai() {
        return jam_mulai;
    }

    public void setJam_mulai(String jam_mulai) {
        this.jam_mulai = jam_mulai;
    }

    public String getJam_akhir() {
        return jam_akhir;
    }

    public void setJam_akhir(String jam_akhir) {
        this.jam_akhir = jam_akhir;
    }
}
